#include <iostream>

using namespace std;

int main()
{
	int MyMoney = 100;
	int Operand = 10;

	cout << MyMoney + Operand << endl;
	cout << MyMoney - Operand << endl;
	cout << MyMoney * Operand << endl;
	cout << MyMoney / Operand << endl;
	cout << MyMoney % Operand << endl;

	cout << 10 + 2 * 20 << endl;
	cout << 6 / 2 * (1 + 2) << endl;
	return 0;
}